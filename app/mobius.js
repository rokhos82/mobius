////////////////////////////////////////////////////////////////////////////////////////////////////
// Mobius Global Namespace Variable
////////////////////////////////////////////////////////////////////////////////////////////////////
var mobius = {};

////////////////////////////////////////////////////////////////////////////////////////////////////
// Mobius Defaults
////////////////////////////////////////////////////////////////////////////////////////////////////
mobius.defaults = {};
mobius.defaults.alert = {
	msg: "",
	type: "warning",
	timeout: undefined
};

////////////////////////////////////////////////////////////////////////////////////////////////////
// Mobius Templates
////////////////////////////////////////////////////////////////////////////////////////////////////
mobius.templates = {};

// Bootstrap UI Alert Template /////////////////////////////////////////////////////////////////////
mobius.templates.alert = function(msg,type,timeout) {
	var alert = this;
	alert.msg = msg;
	alert.type = type;
	alert.timeout = timeout;
	_.defaults(alert,mobius.defaults.alert);
};